#include "main.h"

void	ft_julia_reset(t_fractol *jul)
{
	jul->x1 = -1.5;
	jul->x2 = 1.5;
	jul->y1 = -1.5;
	jul->y2 = 1.5;
	jul->zoom = SIZEX / (jul->x2 - jul->x1);
	jul->precision = 50;
	ft_julia(jul, 0, 0);
}
